                  <p>Es un aparato electrónico, del tamaño de una moneda, que genera impulsos eléctricos que estimulan al corazón para generar un latido cardiaco.</p>

                  <p class="p-info-3">¿Para que sirve un marcapaso?</p>
                  <p>Para estimular el corazón, aumentando la frecuencia de sus latidos o para evitar que deje de latir.</p>

                  <p class="p-info-3">¿Cuando esta indicado el marcapaso?</p>
                  <p>Se encuentra indicado en personas con desmayos (sincope),  frecuencia cardiaca baja (bradicardia), o en arritmias cardiacas.</p>

                  <p class="p-info-3">¿Dónde se coloca el marcapaso?</p>
                  <p>Se coloca debajo de la clavícula (hombro)  izquierdo o derecho</p>

                  <p class="p-info-3">¿Cómo se coloca el marcapaso?</p>
                  <p>Se realiza una pequeña cirugía debajo de la clavícula izquierda o derecha, donde pasa una vena grande (subclavia) y a través de ella, se introducen 1, 2 o 3 cables que van directamente al corazón, y posteriormente se conecta al generador, que es del tamaño de una moneda, dejándolo debajo de la piel<p>

                  <p class="p-info-3">¿Cuánto dura un marcapaso?</p>
                  <p>La duración de un marcapaso varia dependiendo su uso,   el promedio es de 10 años, pero puede variar desde 8 a 12 años</p>

                  <p class="p-info-3">¿Cada cuando se debe de revisar el marcapaso?</p>
                  <p>Existen una computadoras que pueden revisar el sistema de marcapaso, su revisión se denomina Telemetría, y se recomienda su revisión al menos dos veces al año o en caso de sospecha de falla o agotamiento del marcapaso</p>

                  <p class="p-info-3" >¿Qué pasa cuando se agota el marcapaso?</p>
                  <p>Se realiza una nueva cirugía donde se cambia solo el generador del marcapaso, generalmente los cables, si no hay falla registrada, se mantienen. </p>

                  <div class="images-content">
                      <img src="assets/images/enfermedades/Marcapasos_3.png" alt="">
                  </div>

                  <p class="p-info-3" >¿Qué no puedo hacer con el marcapaso?<p>
                  <p>El marcapaso es un aparato muy seguro, solo se recomienda no realizar esfuerzo físico (ejercicio, levantar cosas mayores de 20 kg,  levantar el brazo cercano al marcapaso arriba de la cabeza por 1 mes, y posteriormente realizar sus actividades normales.</p>
                  <p>Posteriormente el marcapaso no limita ninguna actividad como labores del hogar, o ejercicio físico sin embargo se  debe evitar realizar deportes que supongan contacto físico brusco que pueda hacerle caer y/o golpearse en la zona del implante, ya que el dispositivo o los cables podrían resultar dañados.</p>

                  <p class="p-info-3" >¿A que no me puedo acercar con el marcapaso?<p>

                  <ul>
                    <li>   Imanes muy potentes </li>
                    <li>   Resonancia Magnética</li>
                    <li>   Equipos para soldar</li>
                    <li>   Algunos aparatos utilizados por los dentistas</li>
                    <li>   Máquinas de radioterapia para el tratamiento del cáncer</li>
                    <li>   Cables de alta tensión </li>
                  </ul>

                  <p class="p-info-3" >¿A que si me puedo acercar con el marcapaso?</p>
                  <ul>
                    <li> Hornos de Microondas</li>
                    <li> Radios </li>
                    <li> Computadoras</li>
                    <li> Mantas eléctricas</li>
                    <li> Afeitadoras eléctricas</li>
                    <li> Almohadas eléctricas</li>
                    <li> Detectores de metales</li>
                    <li> Televisiones y control remoto</li>
                    <li> Audífonos Inhalámbricos</li>
                    <li> Aparatos de rayos X y Tomografía</li>
                    <li> Equipos de seguridad aeroportuaria</li>
                  </ul>

                  <p class="p-info-3" >¿Puedo utilizar teléfono celular?</p>
                  <p>Los teléfonos celulares no parecen afectar el marcapaso, sin embargo las nuevas tecnologías o Smart phones requieren de una batería mas potente que pudiera generar un campo electromagnético mayor y una interferencia leve, por lo cual se recomienda utilizar el celular del lado opuesto al marcapaso o a una distancia de 15 cm del marcapaso.</p>

                  <div class="images-content">
                      <img src="assets/images/enfermedades/Marcapasos_4.png" alt="">
                  </div>

                  <p class="p-info-3" >¿Qué complicaciones puede tener el implante del marcapaso?</p>
                  <p>Habitualmente tiene pocas complicaciones,  en torno a un 4% en total.</p>
                  <ul>
                    <li>La complicación más frecuente es la formación de un hematoma o “moretón” donde se aloja el marcapasos. Los hematomas se suelen resolver por sí solos; pueden producir dolor o molestias; y únicamente unos pocos son importantes.</li>
                    <li>Infecciones: el riesgo de infección es bajo (tan sólo un 1% de los casos)  debido a que se toman las precauciones necesarias para evitarlas, pero sus consecuencias podrían ser muy importantes si la infección se extiende y afecta al corazón.</li>
                    <li>Punción del pulmón: La vena a través de la cual se colocan los electrodos pasa muy cerca del pulmón, y es posible que se “pinche” la membrana que recubre el pulmón durante el procedimiento. Cuando esto sucede, se produce salida de aire desde el pulmón, acumulándose aire entre el pulmón y la pared interna del tórax lo que puede producir dificultad para respirar. A esta complicación la llamamos neumotórax, y para resolverlo a veces es necesario colocar un tubo en el tórax para extraer el aire acumulado. Con estas medidas, lo habitual es que el proceso se resuelva completamente entre 2 o 4 días sin secuelas, pudiendo darse de alta al paciente.</li>
                    <li>Perforación cardiaca: es la mas grave pero la menos frecuente (<0.1%) se resuelve con cirugía cardíaca, pero afortunadamente, ocurren de forma muy rara.</li>
                  </ul>

                  <p class="p-info-3" >¿Cuáles son los cuidados  posterior a la colocación de un marcapaso?</p>
                  <p>Al día siguiente se realizará la curación de la herida, volviendo a colocar una gasa o “parche”  y se recomienda su aseo cada 48 hrs con agua y jabon, sin que le llegue el agua de una manera directa y volver a cubrir la herida.</p>
                  <p>No se recomienda la inmovilización del brazo por que podría generar mucho dolor en el hombro posteriormente, solo se recomienda no levantarlo por arriba de la cabeza durante un mes</p>
                  <p>Se requiere retiro de puntos 7 a 10 días posterior a la implantación.</p>
                  <p>Durante el primer mes, y en especial durante la primera semana, se debe evitar realizar movimientos bruscos y cargar pesos con el brazo del lado donde se le implantó el marcapasos. En el caso de las mujeres, se recomienda  el uso de sujetador, ya que el peso de las mamas puede producir desplazamientos.</p>
                  <p>Deberá acudir de emergencia si nota los siguientes signos o síntomas</p>
                  <ul>
                    <li>Fiebre (temperatura superior a 38°C).</li>
                    <li>Dolor intenso en zona de marcapasos.</li>
                    <li>Observe supuración (pus) y/o sangrado activo de la herida quirúrgica.</li>
                    <li>Aumente el tamaño del hematoma en la zona donde se alojó el marcapasos.</li>
                    <li>Notar que el generador o los cables se están saliendo de la piel.</li> 
                  </ul>

                  <p class="p-info-3" >¿Puedo Viajar?</p>
                  <p>La mayoría de las personas con marcapasos implantados pueden viajar libremente a menos que tenga otros padecimientos que supongan alguna restricción. La clave para viajar con confianza es planificar el proceso de antemano. Antes de salir de viaje:</p>
                  <ul>
                    <li>Solicite a su médico recomendaciones específicas relativas a los cuidados o la actividad a realizar.</li>
                    <li>Pregúntele a su médico los  pasos que debe dar si siente malestar o experimenta algún síntoma.</li>
                    <li>Lleve siempre con usted la tarjeta de identificación de su marcapasos.</li>
                    <li>Solicite a su médico una copia del impreso que suministra el programador del marcapasos. En algunos casos,  este informe que facilita el programador puede imprimirse en el idioma del país al que planea viajar.</li>
                  </ul>
                  <p>Al pasar por los controles del aeropuerto, usted deberá identificarse como portador de un marcapasos y mostrar al guardia su tarjeta del marcapasos. El guardia podrá pedirle que pase por el detector de metales. Esto, en la mayoría de casos, no afecta al marcapasos, pero es recomendable que usted pase por el detector con un paso continuo y sin detenerse. En caso de que sienta que se marea, aléjese del detector y avise al guardia de seguridad. Si salta la alarma del detector, esto es simplemente porque ha detectado el metal en usted. En este caso, el guardia de seguridad puede que utilice un detector manual de metales, si es así, indíquele al guardia donde tiene colocado el marcapasos y pídale que no pase el detector por encima de él.</p>
                  <p>En la mayoría de los aeropuertos, los protocolos de actuación en caso de pacientes portadores de dispositivos están muy bien definidos, por lo que usted, normalmente, solo deberá identificarse como portador de marcapasos.</p>

                  <p class="p-info-3" >¿Puedo manejar?</p>
                  <p>Depende mucho de la enfermedad por la cual se colocó el marcapaso.</p>
                  <p>Si no hay contraindicación se recomienda que pueda manejar al mes para automovilistas y tres meses para choferes.</p>
