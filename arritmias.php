                <p class="p-info-2">&nbsp;</p>



                    <p class="p-info-3">¿Qué es una arritmia Cardiaca?</p>
                    <p>Una arritmia es cualquier alteración del ritmo normal del corazón.</p>
                    <p>El corazón debe de estar latiendo entre 60 latidos por minuto en reposo y  100 latidos por minutos en actividad.</p>
                    <p>Por lo tanto, las arritmias cardiacas se denominan aquellas que pueden alterar este ritmo del corazón.</p>
                    <p>Se pueden dividir en dos grandes grupos:<p>
                    <ul>
                    <li>Arritmias Lentas o Bradicardias</li>
                    <li>Arritmias Rápidas o  Taquicardias</li>
                    </ul>

                    <p class="p-info-3">¿Qué es una Bradicardia?</p>
                    <p>Es cuando el corazón late muy lento,  menor a 60 latidos por minuto</p>
                    <p>Los síntomas de una bradicardia son: </p>
                    <ul>
                    <li>Mareo</li>
                    <li>Confusión</li>
                    <li>Nauseas, vómitos</li>
                    <li>Dificultad para respirar</li>
                    <li>Sensación de Desmayo  o el desmayo propiamente (síncope)</li>
                    <li>Síncopes recurrentes</li>
                    <li>Estado de Inconciencia (no se puede despertar)</li>
                    </ul>
                    <p>Los síntomas son debidos a una disminución del flujo de sangre al cerebro por lo cual, no puede trabajar adecuadamente produciendo generalmente el síncope</p>

                    <p class="p-info-3">¿Cuál es el tratamiento de las Bradicardias?</p>
                    <p>Hay varios tipos de bradicardias y el tratamiento es individualizado según cada patología, pero en general, la gran mayoría de las bradicardias,  requieren un dispositivo de estimulación cardiaca conocido como Marcapaso, el cual, acelera el corazón.</p>
                    <p>Si usted sufre de algunos de estos síntomas, se recomienda acudir a valoración inmediata con el especialista para decidir la mejor opción terapéutica.</p>

                    <p class="p-info-3">¿Qué es una Taquicardia?</p>
                    <p>Es cuando el corazón , sin causa aparente o súbitamente, late a mas de 100 latidos por minuto. </p>
                    <p>Se puede percibir una sensación de “palpitaciones” muy rápidas o fuertes en el pecho, incluso algunas personas alcanzan a ver como el tórax  palpita en el área del corazón.</p>
                    <p>Las taquicardias pueden llegar hasta latir a 200  y  300 latidos por minuto (¡imagínese lo que es sentir el corazón a esa velocidad¡)</p>

                    <div class="images-content">
                      <img src="assets/images/enfermedades/ARRITMIAS_01.png" alt="">
                    </div> 

                    <p>Pueden iniciar de manera súbita en cualquier momento,  ya sea en reposo, incluso dormidos o realizando ejercicio. </p>

                    <p class="p-info-3">¿Cuál es el tratamiento de las Taquicardias?</p>
                    <p>Su manejo generalmente inicia con medicamentos Antiarrítmicos, para controlarlas en un principio pero su tratamiento final debe ser un Estudio Electrofisiológico para estudiar por dentro del corazón las arritmias y dar un tratamiento definitivo, incluso curarlas. </p>

                    <div class="images-content">
                      <img src="assets/images/enfermedades/ARRITMIAS_02.png" alt="">
                    </div> 


                    <p>Si usted percibe la sensación de palpitaciones o taquicardias, se recomienda acudir inmediatamente a valoración para decidir la mejor opción en su manejo. </p>



