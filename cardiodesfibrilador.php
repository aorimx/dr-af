                <p class="p-info-3">¿Qué es un Desfibrilador Automático Implantable (DAI)?</p>
                <p>Es un dispositivo inteligente que detecta arritmias cardiacas letales, enviando rápidamente una descarga eléctrica al corazón. Esta cambia el ritmo de nuevo a la normalidad. Esto se denomina desfibrilación.</p>

                <p>Su tamaño y grosor son parecidos a un teléfono celular pequeño</p>

                <div class="images-content">
                  <img src="assets/images/enfermedades/cardiodesfibrilador.png" alt="">
                </div>               

                <p class="p-info-3">¿Que es una arritmia letal?</p>
                <p>Las arritmias son alteraciones del latido normal del corazón,  se dividen en dos:  lentas y rápidas. </p>
                <p>Para las arritmias lenta generalmente se debe de tratar con un marcapaso</p>
                <p>Las arritmias rápidas generan taquicardias y están pueden ser tan rápidas que ponen en riesgo la vida por lo cual la única manera de tratarlas será dar una descarga eléctrica o cardioversión. </p>
                <p>Estas arritmias pueden generar Muerte súbita incluso en gente aparentemente sana, como visto recientemente en algunos deportistas que en medio de un partido caen muertos sin causa aparente.</p>

                <p class="p-info-3">¿Cuándo esta indicado colocar un Desfibrilador automático implantable (DAI)?</p>
                <p>Su indicaciones son muy precisas</p>
                <ul>
                  <li>Arritmias cardiacas potencialmente letales.</li>
                  <li>Paciente con taquicardias después de infartos. </li>
                  <li>Pacientes con desmayos (sincopes) por arritmias. </li>
                  <li>Personas que hubieran tenido un familiar que sufrió muerte súbita. (padres, hermanos o hijos).</li>
                </ul>

                <p class="p-info-3">¿Cómo funciona el DAI?</p>
                <p>El aparato tiene la capacidad de detectar una arritmia cardiaca y decidir el mejor tratamiento para la misma, teniendo varias opciones, inclusive puede mandar una descarga eléctrica fuerte al corazón para reiniciar su función, salvando la vida del paciente. Esto lo puede realizar en cuestión de segundos. </p>

                <p class="p-info-3">¿Qué se siente cuando el DAI administre una terapia?</p>
                <p>Se puede sentir palpitaciones, sensación de que el tórax “brinca”. Sin embargo si es necesaria la administración de un choque eléctrico, la mayoría de los pacientes describen el choque eléctrico como una sacudida súbita o un fuerte golpe en el pecho. Algunas personas se desmayan durante un episodio de fibrilación y no sienten nada cuando se administra el choque. Si una persona lo toca en el momento de recibir usted el choque eléctrico, ésta posiblemente sienta una pequeña sacudida de los músculos, pero no le hará daño.</p>
                <p>Se recomienda acudir con su médico lo mas pronto posible posterior a la descarga eléctrica para revisar tanto al paciente como al DAI, y verificar cual fue el motivo de la terapia. </p>

                <p class="p-info-3">¿Los Cardiodesfibriladores (DAI) tiene alarmas? </p>
                <p>Si, algunos dispositivos tienen Alarmas sonoras , como una alarma de un teléfono celular. Si usted escucha esta alerta, no entre en pánico,  es simplemente un aviso de que el aparato detecto alguna anomalía y tiene que ir a revisión.  Generalmente es un aviso de que la batería esta por agotarse. </p>

                <p class="p-info-3">¿Qué tan frecuente se debe de revisar el dispositivo?</p>
                <p>Posterior a la implantación, se recomienda mensualmente durante los primeros 3 meses y posteriormente cada 3 a 4 meses, o antes en caso de documentar una alerta o una descarga eléctrica. </p>

                <p class="p-info-3">¿Cuánto tiempo dura un Cardiodesfibrilador (DAI)?</p>
                <p>La batería dura entre 4 y 8 años, según el número de choques eléctricos o terapias administradas. Cuando la batería se agota, debe implantarse un nuevo DCI. En la mayoría de los casos, no es necesario cambiar los electrodos originales.</p>

                <p class="p-info-3">¿Afectan los aparatos electrónicos al DAI?</p>
                <p>Si, al igual que los marcapasos, hay una serie de aparatos electromagnéticos que pueden afectar, estos se encuentran en listados en el apartado de marcapaso, pero en general son aquellos que generan un campo electromagnético fuerte como la Resonancia Magnética, por lo cual esta contraindicada, a menos de que su dispositivo tenga protección para esta terapia</p>

                <p class="p-info-3">¿Puedo manejar un auto si tengo un DAI?</p>
                <p>La recomendación es que no maneje, debido a que no es posible saber en que momento puede generar una arritmias su corazón  y una descarga eléctrica del DAI, incapacitándolo para poder manejar en ese momento. </p>

                <p class="p-info-3">¿Puedo usar teléfonos celulares si tengo un DCI implantado?</p>
                <p>Al igual que los marcapaso se recomienda mantener el teléfono celular a una distancia de por lo menos 15 cm del DAI. Por lo tanto, cuando utilice el teléfono celular, sosténgalo del lado opuesto del DAI. No lleve el teléfono celular sobre el cinturón o en un bolsillo que se encuentre a una distancia de menos de 15 cm</p>
