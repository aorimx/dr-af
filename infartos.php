                <p class="p-info-3">¿Cómo se trata un infarto?</p>
                <p>Se debe de abrir la arteria que se encuentra ocluida antes de que mate a las paredes del corazón </p>
                <p>Hay dos maneras: </p>
                <ul>
                  <li>Lo ideal realizar un Cateterismo Cardiaco donde se pueden abrir las arterias inmediatamente. Se recomienda antes de las tres horas del infarto </li>
                  <li>Lo segundo  es aplicar medicamentos que pueden destruir el coagulo que está ocluyendo a la arteria y se recomienda antes de las 6 horas del infarto  </li>
                </ul>
                <p class="p-info-3">¿Los infartos son peligrosos?</p>
                <p>Definitivamente ¡¡¡ Siiiii !!!</p>
                <p>Si usted presenta los síntomas o tiene dudas de que se encuentre sufriendo un infarto, acuda inmediatamente a atención médica para su adecuada valoración.</p>
                <p>Recuerde que entre más tiempo pasa , es más difícil salvar al corazón y por consiguiente pone en mayor riesgo su vida.</p>

