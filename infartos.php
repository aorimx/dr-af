                <p class="p-info-2">&nbsp;</p>

                <p class="p-info-3"> ¿Qué es un infarto?</p>
                <p>Es cuando una de las paredes del corazón se muere por falta de sangre debido a que una arteria del corazón se ocluyó</p>

                <p class="p-info-3">¿Los infartos duelen?</p>
                <p>En si un infarto si genera una sensación de dolor por que está generando una muerte de células, pero no se percibe como un dolor propiamente,  se siente una OPRESIÓN o Sensación de algo muy pesado en el pecho que no permite respirar adecuadamente. </p>

                <p class="p-info-3">¿Qué es un preinfarto?</p>
                <p>En términos médicos se conoce como Angina de pecho, y es un aviso de que esta por venir un infarto.</p>
                <p>El dolor se percibe igual, una opresión en el pecho que no permite respirar, pero es de menor intensidad y dura menos de 20 minutos. </p>
                <p>Esto es debido a una disminución del flujo de sangre a las arterias del corazón, sin obstruirlas al 100%, ya que esto generaría un infarto. </p>

                <p class="p-info-3">¿Qué otros síntomas genera un infarto?</p>
                <p>Ademas de la opresión del pecho , los infartos pueden generar: </p>
                <ul>
                  <li>Dificultan severa para respirar</li>
                  <li>Sudor frio (diaforesis)</li>
                  <li>Sensación de opresión del cuello o de la parte interna del brazo izquierdo </li>
                  <li>Mareo </li>
                  <li>Nausea, vómitos y diarrea</li>
                  <li>Desmayos (sincope) y estar completamente inconsciente. </li>
                </ul>


                <p class="p-info-3">¿Cuáles son los factores de riesgo para los infartos?</p>
                <p>Se puede clasificar en mayores y menores</p>
                <p>Los mayores son los que seguramente van a producir infartos, estos son:</p>
                <ul>
                  <li>Ser Diabético descontrolado </li>
                  <li>Hipertensión arterial mal controlada</li>
                  <li>Colesterol y triglicéridos altos</li>
                  <li>Fumar. </li>
                </ul>
                <p>Esto es como comprar los boletos de la lotería, si tengo todos los boletos, obviamente voy a ganar la lotería , en este caso , un infarto. </p>

                <p>Los menores son los que tienen probabilidad de generar infarto,  y son: </p>
                <ul>
                  <li>Sobrepeso y obesidad</li>
                  <li>No realizar ejercicio (menos de 30 minutos tres veces por semana)</li>
                  <li>Café o bebidas energizantes (más de 3 bebidas por día)</li>
                  <li>Ser de carácter irritable </li>
                  <li>Género Masculino</li>
                  <li>Uso de anticonceptivos hormonales por varios años sin suspender. </li>
                </ul>

                <p class="p-info-3">¿Cómo se hace el diagnóstico de un infarto?</p>
                <p>Para saber si una persona se encuentra cursando con un infarto agudo se tienen que cumplir dos de los siguientes tres criterios</p>
                <ul>
                  <li>Síntomas (cuadro clínico)</li>
                  <li>Cambios en un Electrocardiograma</li>
                  <li>Muestra de sangre con enzimas cardiacas elevadas</li>
                </ul>

